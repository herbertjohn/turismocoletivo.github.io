                  <p class="lead">A principal diferença do pequeno moenkhausia uirapuru para os outros lambaris são as cores. O peixe, espécie rara descoberta em 2015, é repleto de tons de azul, amarelo e vermelho em seu corpo. Esta beleza colorida chamou a atenção dos pesquisadores do Museu Zoológico da Universidade de São Paulo (USP) e de ictiólogos da Unicamp, que o coletaram na Cascata do Uirapuru, no município de Nova Lacerda em Mato Grosso.</p>
                  <p>A espécie endêmica, encontrada principalmente na Cascata do Uirapuru e em algumas outras regiões das cabeceiras do Rio Guaporé, pode estar em risco de ser extinta, caso o projeto de construir uma Pequena Central Hidrelétrica (PCH) no Parque Natural do Uirapuru seja colocado em prática. A avaliação quem faz é o ictiólogo Fernando Dagosta, que estuda a fauna dos rios de Mato Grosso e Mato Grosso do Sul.</p>

                  <blockquote><p>“Esta região de Pontes e Lacerda, do Vale do Guaporé, é muito interessante do ponto de vista dos peixes. Lá existem espécies que nem sequer foram catalogadas ainda”, ressalta. “Infelizmente no Brasil existe uma mentalidade ecológica muito baixa em que se faz empreendimentos sem o devido respeito ao meio ambiente”, concluiu o pesquisador, pós-doutor em ictiologia e professor da Universidade Federal da Grande Dourados (UFGD).</p></blockquote>

                  <p>A descoberta do moenkhausia uirapuru foi publicada na revista alemã Ichthyological Exploration of Freshwaters e, segundo Dagosta, pode abrir caminho para novas pesquisas na região. Ele, que é colega dos autores do trabalho, conta que os pesquisadores encontraram um “deserto de coletas” no Uirapuru, local onde poucas espécies foram pesquisadas e catalogadas.</p>
                  <h2>Uirapuru e Itaipu</h2>
                  <p>Dagosta esteve no Parque antes dele ser fechado, no final de 2017. Na ocasião, coletou alguns dos lambaris multicoloridos. O local foi fechado depois de decisão judicial anulando o decreto municipal 577/2011, de desapropriação da área das cachoeiras. A área de 797 hectares foi desapropriada do empresário Antônio Beraldo dos Santos e reconhecido como parque natural pela Secretaria Municipal do Meio Ambiente (Sema) em 2008.</p>
                  <p>As terras são disputadas na Justiça por Celso Padovani, dono das empresas Pronorte Colonização Ltda, e pela Incomex Indústria Comércio e Exportação Ltda. O empresário convenceu o juiz de primeiro grau alegando que o decreto de desapropriação não poderia ter sido feito pelo município, uma vez a aérea detém potencial energético, e que a desapropriação seria responsabilidade da União.</p>
                  <p>Na sentença original, datada de 2012, o juiz Almir Barbosa – atualmente afastado por venda de sentença – comparou o caso ao da Usina de Itaipu. O magistrado entendeu que o potencial energético da área seria maior do que o potencial turístico e ambiental. A sentença foi confirmada recentemente pela desembargadora Maria Erotides Kneip Baranjak da Terceira Câmara Cível.</p>

                  <p>“Entendo que o interesse público do potencial de energia elétrica do local é superior a interesse público da criação do balneário público. Cumpre ressaltar que não haveria qualquer óbice legal para que a usina em plena atividade servisse de ponto turístico, já que a maior usina hidrelétrica do mundo no caso a Usina de Itaipú, que também não é própria para banho é considerada ponto turístico mundialmente”, declarou na época o juiz.</p>
